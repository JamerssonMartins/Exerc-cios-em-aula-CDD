number = int(input("Digite um número: "))
if number >= 1 and number <= 12:
    if number == 1:
        print("Janeiro")
    elif number == 2:
        print("Fevereiro")
    elif number == 3:
        print("Março")
    elif number == 4:
        print("Abril")
    elif number == 5:
        print("Maio")
    elif number == 6:
        print("Junho")
    elif number == 7:
        print("Julho")
    elif number == 8:
        print("Agosto")
    elif number == 9:
        print("Setembro")
    elif number == 10:
        print("Outubro")
    elif number == 11:
        print("Novembro")
    elif number == 12:
        print("Dezembro")
    else:
        print("Opção inválida, digite um número de 1 a 12")